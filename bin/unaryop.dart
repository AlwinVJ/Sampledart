// This code shows the functionality of unary operators
void main() {
// Postfix and Prefix operators
  var x = 1;
  print(++x); //Prefix increment operator
  print(--x);
  print(x++);
  print(x--);
  var y = 1;
  print(y);
  print(y++);
  print(y++);
  print(y++);
  print(y++);
  print(y);
  print(y--);
  print(y--);
  print(y--);
  print(--y);
}
