void main() {
  var a = 10;
  var b = 20;
  print('The result of addition is ${a + b}');
  print('The result of subtraction is ${a - b}');
  print('The result of multiplication is ${a * b}');
  print('The result of division is ${a / b}');
  print('The result of tilt division is ${a ~/ b}');
  print('The result of modulus division is ${a % b}');
  print('The result of unary minus operator is ${-(a + b)}');
